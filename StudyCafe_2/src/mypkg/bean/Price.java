package mypkg.bean;

public class Price {
	private String id;
	private int sumtotal;

	public Price() {
	}
	
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getSumtotal() {
		return sumtotal;
	}

	public void setSumtotal(int sumtotal) {
		this.sumtotal = sumtotal;
	}	

}
